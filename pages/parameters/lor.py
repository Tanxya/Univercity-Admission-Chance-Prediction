import streamlit as st

def main():
    st.title("Letter of Recommendation (LOR)")
    st.markdown("""
    ### LOR (Letter of Recommendation)

    **LOR** is a rating (0.0 to 5.0) reflecting the strength and impact of your Letter of Recommendation.

    #### Importance:
    - Provides an external perspective on your qualifications and abilities.
    - Highlights your academic and professional achievements.
    - Validates your skills and character through testimonials from professors or employers.

    #### Components of a Strong LOR:
    - **Introduction**: The recommender introduces themselves and their relationship with you.
    - **Academic/Professional Performance**: The recommender discusses your performance in academic or professional settings.
    - **Skills and Qualities**: The recommender highlights your key skills, strengths, and personal qualities.
    - **Specific Examples**: The recommender provides specific examples to support their claims about your abilities.
    - **Conclusion**: The recommender summarizes their recommendation and expresses confidence in your potential.

    #### Tips:
    - Choose recommenders who know you well and can speak to your strengths.
    - Provide your recommenders with relevant information to help them write a detailed LOR.
    - Follow up with your recommenders to ensure they submit the LOR on time.
    - Thank your recommenders for their support and assistance.
    """)

if __name__ == "__main__":
    main()
