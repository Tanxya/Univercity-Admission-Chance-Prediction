import streamlit as st

def main():
    st.title("Statement of Purpose (SOP)")
    st.markdown("""
    ### SOP (Statement of Purpose)

    **SOP** is a rating (0.0 to 5.0) reflecting the quality of your Statement of Purpose.

    #### Importance:
    - Provides insight into your academic and professional background.
    - Demonstrates your motivation and goals for pursuing higher education.
    - Allows you to showcase your writing skills and ability to articulate your aspirations.

    #### Components of a Strong SOP:
    - **Introduction**: Briefly introduce yourself and your goals.
    - **Academic Background**: Discuss your academic achievements and relevant coursework.
    - **Professional Experience**: Highlight your work experience and skills.
    - **Motivation**: Explain why you are interested in the program and how it aligns with your goals.
    - **Future Goals**: Outline your long-term career objectives and how the program will help you achieve them.

    #### Tips:
    - Be clear and concise, avoiding unnecessary jargon.
    - Tailor your SOP to each program, emphasizing why you are a good fit.
    - Proofread your SOP multiple times to ensure it is free of errors.
    - Seek feedback from mentors or peers to improve your SOP.
    """)

if __name__ == "__main__":
    main()
